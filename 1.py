#加算
# 整数
print(1+2)   # 3
# 小数
print(1.5+2.5) # 4.0

#減算
# 整数
print(1-2)  # -1
# 小数
print(1.5-2.5) # -1.0

#乗算
# 整数
print(1*2)  #2
# 小数
print(1.5*2.5)  #3.75

#除算
# 整数
print(2/1)   # 2.0
# 小数
print(2.5/1.5)  # 1.66・・・7

#　商
print(5//2) # 2
# 余り
print(5%2)  # 1

#　べき乗
print(2 ** 3)   # 8

#exception####
# 整数と小数
print(1+0.5) # 1.5
# 0割り
print(1/0) # Error ZeroDivision

#練習問題
print(2**2-1*(1+0.5)) # 2.5